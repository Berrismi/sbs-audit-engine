// Smoke-test helper: generate a valid random answer YAML for the alpha.43
// questionnaire so we can run `sf security review run --questionnaire ...`
// non-interactively against a real org. Intentionally lives under the plugin
// dir so node resolves `yaml` and the engine via local node_modules.

import { writeFile } from 'node:fs/promises';
import { stringify } from 'yaml';
import { REGISTRY } from '@hellomavens/security-review-for-salesforce-engine/questionnaire';

function randItem(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

const answers = {};
for (const q of REGISTRY.questions) {
  // Force CPORTAL scope = yes so portal questions are exercised, not skipped.
  if (q.id === 'Q-SCOPE-CPORTAL') {
    answers[q.id] = { kind: 'boolean', value: true };
    continue;
  }
  switch (q.kind) {
    case 'boolean': {
      const r = Math.random();
      if (q.allowIdk && r > 0.9) answers[q.id] = { kind: 'idk' };
      else answers[q.id] = { kind: 'boolean', value: r < 0.6 };
      break;
    }
    case 'choice':
      answers[q.id] = { kind: 'choice', value: randItem(q.options).value };
      break;
    case 'multi_choice': {
      const k = 1 + Math.floor(Math.random() * Math.min(2, q.options.length));
      const shuffled = [...q.options].sort(() => Math.random() - 0.5);
      answers[q.id] = { kind: 'multi_choice', values: shuffled.slice(0, k).map((o) => o.value) };
      break;
    }
    case 'numeric_range':
      answers[q.id] = { kind: 'numeric_range', value: randItem(q.options).value };
      break;
    case 'free_text':
      answers[q.id] = { kind: 'free_text', value: 'Healthcare SaaS (smoke test)' };
      break;
  }
}

const out = process.argv[2] ?? '/tmp/random-answers.yml';
const doc = {
  metadata: {
    alias: 'hm-cli-validation',
    registryVersion: REGISTRY.version,
    sbsVersion: REGISTRY.sbsVersion,
    savedAt: new Date().toISOString(),
    note: 'Generated by alpha.43 smoke-test script — random valid answers',
  },
  answers,
};

await writeFile(out, stringify(doc));
console.log(`Wrote ${out} with ${Object.keys(answers).length} answers`);
