import { describe, expect, it } from 'vitest';
import { evaluate } from '../../src/evaluators/acs-003';
import { makeControlFixture } from '../fixtures/control';
import { describeBooleanEvaluator } from './_shared';

describeBooleanEvaluator({
  controlId: 'SBS-ACS-003',
  questionId: 'Q-ACS-003',
  evaluate,
});

describe('SBS-ACS-003 evaluator (SOQL evidence path)', () => {
  const control = makeControlFixture('SBS-ACS-003');
  const QUERY_ID = 'acs-003-approve-uninstalled-connected-apps-via-permsets';

  it('returns pass+high when no active users are granted Approve Uninstalled Connected Apps via permset', () => {
    const result = evaluate({
      control,
      evidence: [{ source: 'soql', query: '...', query_id: QUERY_ID, rows: [] }],
    });
    expect(result.status).toBe('pass');
    expect(result.confidence).toBe('high');
    expect(result.evidence_used).toEqual(['soql']);
  });

  it('returns inconclusive+high when Approve Uninstalled Connected Apps assignments are inventoried', () => {
    const result = evaluate({
      control,
      evidence: [
        {
          source: 'soql',
          query: '...',
          query_id: QUERY_ID,
          rows: [
            {
              AssigneeId: '005xx',
              'Assignee.Username': 'admin@example.com',
              'PermissionSet.Label': 'Connected App Admin',
            },
            {
              AssigneeId: '005yy',
              'Assignee.Username': 'integration.dev@example.com',
              'PermissionSet.Label': 'Connected App Admin',
            },
          ],
        },
      ],
    });
    expect(result.status).toBe('inconclusive');
    expect(result.confidence).toBe('high');
    expect(result.evidence_used).toEqual(['soql']);
    expect(result.findings[0]).toContain('2 active user-permset assignment');
  });

  it('SOQL evidence wins over questionnaire when both are present', () => {
    const result = evaluate({
      control,
      evidence: [
        {
          source: 'questionnaire',
          question_id: 'Q-ACS-003',
          answer: { kind: 'boolean', value: true },
        },
        { source: 'soql', query: '...', query_id: QUERY_ID, rows: [] },
      ],
    });
    expect(result.confidence).toBe('high');
    expect(result.evidence_used).toEqual(['soql']);
  });

  it('falls back to questionnaire low-confidence when SOQL evidence has different query_id', () => {
    const result = evaluate({
      control,
      evidence: [
        { source: 'soql', query: '...', query_id: 'some-other-query', rows: [] },
        {
          source: 'questionnaire',
          question_id: 'Q-ACS-003',
          answer: { kind: 'boolean', value: true },
        },
      ],
    });
    expect(result.confidence).toBe('low');
    expect(result.evidence_used).toEqual(['questionnaire']);
  });
});
